class Operations
{
    public double ComputeSum(double num1, double num2, double num3)
    {
        double sum = 0;
        sum = num1 + num2 + num3;
        return sum;
    } 

    public double ComputeSum(double num1, double num2)
    {
        double sum = 0;
        sum = num1 + num2;
        return sum;
    } 

    public int ComputeSum(int num1, int num2)
    {
        int sum = 0;
        sum = num1 + num2;
        return sum;
    } 
}


